function handleInputkeyup(event) {
  const input = event.target;
  validateInput(input);
  loadState(input);
  checkIfCompleted();
}

function validateInput(input) {
  if (input.value.toUpperCase() === correctAnswers[input.id]) {
    input.style.backgroundColor = "#C3F2C7";
    localStorage.setItem(input.id, "completed");
  }
}

function loadState(input) {
  if (localStorage.getItem(input.id) === "completed") {
    input.value = correctAnswers[input.id];
    input.style.backgroundColor = "#C3F2C7";
    input.disabled = true;
  }
}

function checkIfCompleted() {
  const inputs = document.querySelectorAll('input[type="text"]');
  if (Array.from(inputs).every((input) => input.disabled === true)) {
    displayFinalMessage();
  }
}

function displayFinalMessage() {
  if (!document.querySelector(".claveFinal")) {
    let claveFinal = document.createElement("div");
    claveFinal.className = "claveFinal";
    claveFinal.id = "claveFinal";
    claveFinal.textContent = "Congratulations! Click here to see hint.";
    claveFinal.onclick = function () {
      // Obtener el iframe desde dentro del iframe
      var iframe = window.frameElement;
      iframe.style.width="62.5em";
      iframe.style.height="68.75em";
      
      // Obtener el contenido del iframe
      var iframeContent = iframe.contentDocument || iframe.contentWindow.document;
      
      // Obtener el modal dentro del iframe
      var modalDentroIframe = iframeContent.querySelector('.modal');
      
      // Ajustar el tamaño del modal dentro del iframe
      modalDentroIframe.style.width = '56.25em'; // Ajusta el ancho según sea necesario
      modalDentroIframe.style.height = '56.25em'; // Ajusta la altura según sea necesario

      // Mostrar el modal
      modalDentroIframe.style.display = 'block';
    };
    document.body.appendChild(claveFinal);
  }
}

